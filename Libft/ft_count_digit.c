#include "libft.h"

int	ft_count_digit(int n)
{
	int len;

	len = 0;
	if (n < 0)
	{
		len++;
		n *= -1;
	}
	if (n == 0)
		return (1);
	while ((n / 10) != 0)
	{
		n /= 10;
		len++;
	}
	if (n % 10 != 0)
		len++;
	return (len);
}
