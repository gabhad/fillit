int	ft_sqrt(int nb)
{
	int i;
	int sum;

	i = 0;
	sum = 1;
	while (sum > 0)
	{
		sum = nb - (i * i);
		i++;
	}
	if (sum < 0)
		return (0);
	else
		return (i - 1);
}
